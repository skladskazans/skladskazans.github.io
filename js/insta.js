"use strict";

//6
var imgs6 = document.querySelector(".modali1");
var otvetFull6 = document.querySelector(".modali1-full");

imgs6.addEventListener('click', function (event) {
  return imgClick6(event);
});

function imgClick6(event) {
  var element = event.target;
  var img = element.getAttribute('src');

  if (img) {
    otvetFull6.setAttribute('src', img);
  }
}

